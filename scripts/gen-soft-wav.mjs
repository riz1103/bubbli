/**
 * Writes soft sine chimes for Bubbli (run: node scripts/gen-soft-wav.mjs)
 * Also generates src/features/games/sounds/tonePoolSources.generated.ts for Metro.
 */
import fs from 'fs';
import path from 'path';
import { fileURLToPath } from 'url';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const root = path.join(__dirname, '..');
const outDir = path.join(root, 'assets', 'sounds');
fs.mkdirSync(outDir, { recursive: true });

const TONE_POOL_COUNT = 50;

function writeTone(filename, { freq, vol, seconds, sampleRate = 16000, harmonic = 0 }) {
  const numSamples = Math.floor(sampleRate * seconds);
  const dataSize = numSamples * 2;
  const buffer = Buffer.alloc(44 + dataSize);

  buffer.write('RIFF', 0);
  buffer.writeUInt32LE(36 + dataSize, 4);
  buffer.write('WAVE', 8);
  buffer.write('fmt ', 12);
  buffer.writeUInt32LE(16, 16);
  buffer.writeUInt16LE(1, 20);
  buffer.writeUInt16LE(1, 22);
  buffer.writeUInt32LE(sampleRate, 24);
  buffer.writeUInt32LE(sampleRate * 2, 28);
  buffer.writeUInt16LE(2, 32);
  buffer.writeUInt16LE(16, 34);
  buffer.write('data', 36);
  buffer.writeUInt32LE(dataSize, 40);

  let t = 0;
  const dt = 1 / sampleRate;
  const decay = numSamples * 0.35;
  for (let i = 0; i < numSamples; i++) {
    const env = Math.exp(-i / decay);
    const fundamental = Math.sin(2 * Math.PI * freq * t);
    const partial =
      harmonic > 0 ? harmonic * 0.32 * Math.sin(2 * Math.PI * freq * 2 * t) : 0;
    const s = (fundamental + partial) * vol * env;
    const clamped = Math.max(-1, Math.min(1, s));
    buffer.writeInt16LE(Math.round(clamped * 32767), 44 + i * 2);
    t += dt;
  }

  fs.writeFileSync(path.join(outDir, filename), buffer);
  console.log('wrote', path.join(outDir, filename));
}

writeTone('soft-success.wav', { freq: 523.25, vol: 0.22, seconds: 0.14 });
writeTone('soft-soft.wav', { freq: 392, vol: 0.12, seconds: 0.06 });

/** Legacy four tones (kept for any external refs) */
writeTone('tone-game-1.wav', { freq: 220, vol: 0.2, seconds: 0.12 });
writeTone('tone-game-2.wav', { freq: 330, vol: 0.2, seconds: 0.12 });
writeTone('tone-game-3.wav', { freq: 440, vol: 0.2, seconds: 0.12 });
writeTone('tone-game-4.wav', { freq: 660, vol: 0.18, seconds: 0.1 });

/** Sounds game — 50 distinct gentle chimes, log-spread ~200–920 Hz + timbre jitter */
const fMin = 200;
const fMax = 920;
for (let i = 0; i < TONE_POOL_COUNT; i++) {
  const t = TONE_POOL_COUNT <= 1 ? 0 : i / (TONE_POOL_COUNT - 1);
  const freq = fMin * Math.pow(fMax / fMin, t);
  const vol = 0.17 + (i % 5) * 0.012;
  const seconds = 0.1 + (i % 3) * 0.015;
  const harmonic = i % 4 === 1 ? 0.38 : i % 4 === 3 ? 0.22 : 0;
  writeTone(`tone-pool-${i + 1}.wav`, { freq, vol, seconds, harmonic });
}

const tsPath = path.join(
  root,
  'src',
  'features',
  'games',
  'sounds',
  'tonePoolSources.generated.ts'
);
const requires = Array.from(
  { length: TONE_POOL_COUNT },
  (_, i) => `  require('../../../../assets/sounds/tone-pool-${i + 1}.wav'),`
).join('\n');
const ts = `/**
 * Metro static asset map — generated by scripts/gen-soft-wav.mjs (do not edit by hand).
 */
export const TONE_POOL_COUNT = ${TONE_POOL_COUNT} as const;

export const TONE_POOL_SOURCES = [
${requires}
] as const;
`;
fs.writeFileSync(tsPath, ts);
console.log('wrote', tsPath);
